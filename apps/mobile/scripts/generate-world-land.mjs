// Build-time generator for the Family map's land outline (docs/spec/09, ADR 0009).
//
// Reads Natural Earth land-110m from world-atlas (a devDependency), decodes the
// topojson to GeoJSON once, simplifies it, and writes a compact TS module of
// [lng, lat] rings. The app renders those rings directly with react-native-svg,
// so neither world-atlas nor topojson-client ships in the bundle: they exist only
// to produce this file. Re-run with `pnpm --filter mobile gen:map` if the source
// data or the simplification thresholds change.

import { readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { feature } from 'topojson-client';

const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));

// land-110m, not countries: the mockup draws borderless soft continents, and land
// is smaller. 110m is the coarsest Natural Earth tier, right for a world-scale
// decorative map (finer tiers would be wasted at ~360px wide).
const topoPath = require.resolve('world-atlas/land-110m.json');
const topology = JSON.parse(readFileSync(topoPath, 'utf8'));
const land = feature(topology, topology.objects.land);

// One decimal of lng/lat is ~11 km: far finer than a pixel at world scale, and it
// roughly halves the string length versus full precision.
const round = (n) => Math.round(n * 10) / 10;

// Drop rings whose bounding box is smaller than this (degrees): the long tail of
// tiny islands that render as sub-pixel specks but cost real bytes.
const MIN_SPAN = 2.5;

function ringSpan(ring) {
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;
  for (const [x, y] of ring) {
    if (x < minX) minX = x;
    if (x > maxX) maxX = x;
    if (y < minY) minY = y;
    if (y > maxY) maxY = y;
  }
  return Math.max(maxX - minX, maxY - minY);
}

// Collapse consecutive points that round to the same coordinate (simplification
// leaves many duplicates), and drop a ring that collapses below a triangle.
function compactRing(ring) {
  const out = [];
  let prevX = null;
  let prevY = null;
  for (const [lng, lat] of ring) {
    const x = round(lng);
    const y = round(lat);
    if (x !== prevX || y !== prevY) {
      out.push(x, y);
      prevX = x;
      prevY = y;
    }
  }
  return out;
}

// land-110m decodes to a FeatureCollection of one MultiPolygon; normalize to the
// list of polygons whichever shape topojson-client returns.
const geometries = land.features
  ? land.features.map((f) => f.geometry)
  : [land.geometry];
const polygons = geometries.flatMap((g) =>
  g.type === 'MultiPolygon' ? g.coordinates : [g.coordinates],
);

const rings = [];
for (const polygon of polygons) {
  // Outer ring only (index 0): inland-sea holes are invisible at this scale.
  const outer = polygon[0];
  if (ringSpan(outer) < MIN_SPAN) continue;
  const flat = compactRing(outer);
  if (flat.length >= 6) rings.push(flat); // >= 3 points
}

rings.sort((a, b) => b.length - a.length);

const body = `// GENERATED by scripts/generate-world-land.mjs from Natural Earth land-110m
// (world-atlas, public domain). Do not edit by hand; re-run \`pnpm gen:map\`.
//
// Each ring is a flat [lng0, lat0, lng1, lat1, ...] outline in degrees. The Family
// map projects these with an equirectangular projection (see familyMap.ts), the
// same one the branch/testimony pins use, so pins land on the coastline.

export const WORLD_LAND: readonly (readonly number[])[] = ${JSON.stringify(rings)};
`;

const outPath = resolve(here, '../src/features/family/world-land.ts');
writeFileSync(outPath, body);

const bytes = Buffer.byteLength(body, 'utf8');
console.log(
  `wrote ${rings.length} rings, ${(bytes / 1024).toFixed(1)} KB -> ${outPath}`,
);
